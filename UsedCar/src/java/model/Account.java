package model;

/**
 *
 * @author yacth_Mon
 */
public class Account {
    int id;
    String name;
    boolean admin;

    public Account(int id, String name, boolean admin) {
        this.id = id;
        this.name = name;
        this.admin = admin;
    }

    public Account() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isAdmin() {
        return admin;
    }

    public void setAdmin(boolean admin) {
        this.admin = admin;
    }  
    
    
}
